#!/usr/bin/env node
/**
 * Writes config.js in this directory for static hosting (Vercel).
 *
 * Set in Vercel → Settings → Environment Variables:
 *   NEXT_PUBLIC_API_BASE  (recommended; no trailing slash)
 *   PUBLIC_API_BASE
 *   API_BASE
 *
 * Build: npm run build (see package.json)
 */
const fs = require('fs');
const path = require('path');

const base = (
  process.env.NEXT_PUBLIC_API_BASE ||
  process.env.PUBLIC_API_BASE ||
  process.env.API_BASE ||
  ''
).trim();

const out = path.join(__dirname, '..', 'config.js');
const content = `/**
 * Backend base for \`/api/*\` only (no trailing slash). \`/data/*\` is same-origin (this site).
 * Generated by scripts/write-vercel-config.js when NEXT_PUBLIC_API_BASE (etc.) is set.
 */
window.__API_BASE__ = ${JSON.stringify(base)};
`;

fs.writeFileSync(out, content, 'utf8');
console.log('Wrote', out, base ? `(API base: ${base})` : '(empty __API_BASE__)');
